def cups_of_drinks(cups_coffee, cups_tea):
    print(f"There's {cups_coffee} cups of coffee \n "
          f"and {cups_tea} cups of tea.")
    return


print("Використовуємо множення: ")
cups_of_drinks(18 * 3, 13 * 1.5)


print("Створимо нові змінні окремо: ")
amount_coffee = 11
amount_tea = 12

cups_of_drinks(amount_coffee, amount_tea)



