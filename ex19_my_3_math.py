# Standard library functions are the built-in functions
# that can be used directly in program:
# - print() - prints the string inside the quotation marks;
# - sqrt() - returns the square root of a number (is defined inside the 'math' module);
# - pow() - returns the power of a number (підносить до степені).

# 'sqrt()' is defined inside the 'math' module:
import math

square_root = math.sqrt(4)
print('Square Root of 4 is', square_root)

# 3 підноситься до степеня 2
power = pow(3, 2)
print('2 to the power 3 is', power)